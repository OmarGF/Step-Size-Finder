%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%   SYSTEM:                                                               %
%   OSCILLATOR BASED ON CASE S SPROTT                                     %
%   Dy(1) = -y(1)-A*y(2)                                                  %
%   Dy(2) = y(1)+y(3)^2                                                   %
%   Dy(3) = B+y(1)                                                        %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function fy = osc_cases(y)
A = 4; B = 1;

n = length(y);
fy = zeros(n,1);

%%ODE SYSTEM
fy = [-y(1)-A*y(2);
       y(1)+y(3)^2;
       B+y(1)];